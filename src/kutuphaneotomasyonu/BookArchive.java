    public void bookView() {
        model.setRowCount(0);
        ArrayList<Book> books = new ArrayList<Book>();
        books = op.BookCome();

        if (books != null) {

            for (Book bookx : books) {
                Object[] add = {
                    bookx.getBook_name(), bookx.getBook_writer(), bookx.getBook_type(), bookx.getBook_publisher()
                };
                model.addRow(add);
            }
        }
    }

